public class DataTypesVariables {
    public static void main(String[] args){
        //This is the variables

        //Primitive data types
        int num1 = 50;
        int num2 = 60;
        byte num3 = 50;
        short num4 = 65;
        char letter = 'C';

        //getting the boolean
        boolean newnum = num1 > num2; //calling the boolean
        System.out.println("Boolean: "+newnum);

        //getting the byte
        byte newnum1 = num3;
        System.out.println("Byte: " + newnum1); //calling the byte

        //getting the short
        short newnum2 = num4;
        System.out.println("Short: " + newnum2);

        //getting the char
        char letterFinal = letter;
        System.out.println("Character: " + letterFinal);
    }
}
